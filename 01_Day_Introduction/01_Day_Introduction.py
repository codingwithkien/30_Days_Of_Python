# Introduction

print(5 + 3)   # addition(+)
print(4 - 1)   # subtraction(-)
print(2 * 3)   # multiplication(*)
print(6 / 2)   # division(/)
print(2 ** 2)  # exponential(**)
print(3 % 2)   # modulus(%)
print(6 // 2)  # Floor division operator(//)

# Checking data types

print(type(10))                  # Int
print(type(3.14))                # Float
print(type(1 + 3j))              # Complex
print(type('Kien'))              # String
print(type([1, 2, 3]))           # List
print(type({'name':'Kien'}))     # Dictionary
print(type({9.8, 3.14, 2.7}))    # Set
print(type((9.8, 3.14, 2.7)))    # Tuple
